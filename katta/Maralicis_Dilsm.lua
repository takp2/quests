function event_say(e)
	if(e.message:findi("hail")) then
		e.self:Say("Well met adventurer! I am Maralicis Dilsm, Governor of Health and Martial Training here in Katta Castellum. It is important for members of the Validus Custodus to learn to use their bodies as weapons and not rely solely on the edge of a sword or a skin of steel. Enemies come in many shapes and forms and not all of them will be faced when expected, one must know how to fight unprepared.");
	end
end
